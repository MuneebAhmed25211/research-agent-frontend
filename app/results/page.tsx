"use client";

import { Suspense } from "react";
import { useEffect, useState } from "react";
import { useSearchParams, useRouter } from "next/navigation";
import ReactMarkdown from "react-markdown";
import remarkGfm from "remark-gfm";
import { runResearch } from "../api";

// Separate component that uses useSearchParams
function ResultsContent() {
  const searchParams = useSearchParams();
  const router = useRouter();
  const topic = searchParams.get("topic") || "";

  const [report, setReport] = useState("");
  const [plan, setPlan] = useState("");
  const [loading, setLoading] = useState(true);
  const [error, setError] = useState("");

  useEffect(() => {
    if (!topic) return;
    const fetchReport = async () => {
      try {
        setLoading(true);
        const data = await runResearch(topic);
        setReport(data.final_report);
        setPlan(data.research_plan);
      } catch (err) {
        setError("Something went wrong. Please try again.");
      } finally {
        setLoading(false);
      }
    };
    fetchReport();
  }, [topic]);

  if (loading) {
    return (
      <main className="min-h-screen bg-gradient-to-br from-slate-900 via-blue-950 to-slate-900 flex flex-col items-center justify-center gap-6">
        <div className="w-12 h-12 border-4 border-blue-500 border-t-transparent rounded-full animate-spin" />
        <p className="text-slate-300 text-lg">Researching: <span className="text-blue-400 font-medium">{topic}</span></p>
        <p className="text-slate-500 text-sm">This takes 30–60 seconds...</p>
      </main>
    );
  }

  if (error) {
    return (
      <main className="min-h-screen bg-slate-900 flex items-center justify-center">
        <div className="text-center">
          <p className="text-red-400 text-lg mb-4">{error}</p>
          <button onClick={() => router.push("/home")} className="px-4 py-2 bg-blue-600 text-white rounded-lg">
            Try Again
          </button>
        </div>
      </main>
    );
  }

  return (
    <main className="min-h-screen bg-gradient-to-br from-slate-900 via-blue-950 to-slate-900 p-6">
      <div className="max-w-4xl mx-auto">
        <div className="flex items-center justify-between mb-8">
          <button onClick={() => router.push("/home")} className="text-slate-400 hover:text-white transition-colors text-sm">
            ← New Research
          </button>
          <span className="text-slate-500 text-sm">AI Research Agent</span>
        </div>
        <h1 className="text-3xl font-bold text-white mb-2">{topic}</h1>
        <p className="text-slate-400 mb-8 text-sm">Generated by AI Research Agent</p>
        <div className="bg-white/5 border border-white/10 rounded-2xl p-6 mb-6">
          <h2 className="text-blue-400 font-semibold text-sm uppercase tracking-wider mb-3">Research Plan</h2>
          <p className="text-slate-300 text-sm whitespace-pre-line">{plan}</p>
        </div>
        <div className="bg-white/5 border border-white/10 rounded-2xl p-6 prose prose-invert prose-blue max-w-none">
          <ReactMarkdown remarkPlugins={[remarkGfm]}>{report}</ReactMarkdown>
        </div>
      </div>
    </main>
  );
}

// Main page wraps everything in Suspense
export default function ResultsPage() {
  return (
    <Suspense fallback={
      <main className="min-h-screen bg-slate-900 flex items-center justify-center">
        <div className="w-12 h-12 border-4 border-blue-500 border-t-transparent rounded-full animate-spin" />
      </main>
    }>
      <ResultsContent />
    </Suspense>
  );
}